# Leia uma velocidade em m/s, 
# calcule e escreva esta velocidade em km/h. (Vkm/h = Vm/s * 3.6)

# Entrada
velocidade = float(input('Digite o valor da velocidade em (m/s): '))
# Processamento
conversao = velocidade * 3.6
# Saída
print(f'O valor da velocidade {velocidade}m/s em km/h é {conversao} km/h \n')