# Leia 3 números, calcule e escreva a soma dos 2 primeiros e a diferença entre os 2 últimos.

# Entrada
n1 =  int(input('Digite o primeiro número: '))
n2 =  int(input('Digite o segundo número: '))
n3 =  int(input('Digite o terceiro número: '))
# Processamento

soma = n1 + n2
diferenca = n2 - n3

# Saída
print(f'A soma dos dois primeiros números é igual a {soma}, a diferença entre os 2 últimos números é igual a {diferenca}')